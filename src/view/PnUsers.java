    private void btSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSearchActionPerformed
        TableRowSorter sorter = new TableRowSorter(tbMain.getModel());
        tbMain.setRowSorter(sorter);

        String id = tfIdF.getText();
        String name = tfNameF.getText();
        String hoten = tfFullNameF.getText();
        boolean active = cbActiveF.isSelected();
        ArrayList<RowFilter<AbstractTableModel, Object>> alFilters = new ArrayList<>();

        RowFilter<AbstractTableModel, Object> filterId = RowFilter.regexFilter("^" + id + "$", 0); //0 là số cột id
        alFilters.add(filterId);
        RowFilter<AbstractTableModel, Object> filterName = RowFilter.regexFilter(name, 1);
        alFilters.add(filterName);
        RowFilter<AbstractTableModel, Object> filterHoTen = RowFilter.regexFilter(hoten, 2);
        alFilters.add(filterHoTen);
        if (cbActiveF.isEnabled()) {
            RowFilter<AbstractTableModel, Object> filterActive = new RowFilter<AbstractTableModel, Object>() {
                @Override
                public boolean include(RowFilter.Entry<? extends AbstractTableModel, ? extends Object> entry) {
                    // entry.getValue(3);
                    if (active == (boolean) entry.getValue(3)) {
                        return true;
                    }
                    return false;
                }
            };
            alFilters.add(filterActive);
        }

        RowFilter<AbstractTableModel, Object> filterAnd = RowFilter.andFilter(alFilters);
        sorter.setRowFilter(filterAnd);


    }//GEN-LAST:event_btSearchActionPerformed
    public boolean isValid(Users objUsers, String func) {
        boolean result = true;

        switch (func) {
            case "add":
                if (objUsers.getUserName().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Chưa nhập tên");
                    result = false;
                }
                break;
            case "del":
                if (objUsers.getId_user() <= 0) {
                    JOptionPane.showMessageDialog(null, "Chưa chọn dòng để xóa");
                    result = false;
                }
                break;
            case "edit":
                if (objUsers.getId_user() <= 0) {
                    JOptionPane.showMessageDialog(null, "Nhập id");
                    result = false;
                }
                if (objUsers.getUserName().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Nhập tên danh mục");
                    result = false;
                }
        }
        return result;
    }


