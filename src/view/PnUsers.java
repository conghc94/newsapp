    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed

        String userName = tfUserName.getText();
        String password = tfPassword.getText();
        boolean action = cbAction.isSelected();
        String fullName = tfFullName.getText();

        Users objUsers = new Users(0, userName, password, fullName, action);
        if (isValid(objUsers, "add")) {
            int result = controller.addItem(objUsers);
            if (result > 0) {
                JOptionPane.showMessageDialog(null, "Them thanh cong");
            } else {
                JOptionPane.showMessageDialog(null, "Xay ra loi");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Vui long nhap ten");
        }
    }//GEN-LAST:event_btAddActionPerformed

